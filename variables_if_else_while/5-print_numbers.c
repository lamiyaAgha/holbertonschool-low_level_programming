#include <stdio.h>
#include <stdlib.h>
/**
 * main - Entry point
 *
 * Description: This program generates a random number and
 * determines whether it is positive, zero, or negative.
 *
 * Return: Always 0
 */

int main(void)
{
int number = 0;
while (number <= 9)
{
printf("%d", number);
number++;
}
printf("\n");
return (0);
}
